package bean;

public class QuestionBoard {
	String userQuestionBoardNum;
	String userID;
	String title;
	String contents;
	String writeDate;
	String isReply;
	public String getUserQuestionBoardNum() {
		return userQuestionBoardNum;
	}
	public void setUserQuestionBoardNum(String userQuestionBoardNum) {
		this.userQuestionBoardNum = userQuestionBoardNum;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContents() {
		return contents;
	}
	public void setContents(String contents) {
		this.contents = contents;
	}
	public String getWriteDate() {
		return writeDate;
	}
	public void setWriteDate(String writeDate) {
		this.writeDate = writeDate;
	}
	public String getIs_reply() {
		return isReply;
	}
	public void setIs_reply(String isReply) {
		this.isReply = isReply;
	}
	
	
	
}
